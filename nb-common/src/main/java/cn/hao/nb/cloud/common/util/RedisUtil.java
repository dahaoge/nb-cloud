    /**
     * 获取自增/自减值
     *
     * @param key
     * @param item
     * @return
     */
    public Long hGetIncrement(String key, String item) {
        byte[] rawKey = rawKey(key);
        byte[] rawHashKey = rawHashKey(item);
        byte[] rawHashValue = execute(connection -> connection.hGet(rawKey, rawHashKey), true);
        if (Objects.nonNull(rawHashValue) && rawHashValue.length > 0)
            return new Long(new String(rawHashValue));
        return null;
    }

    public Map hGetAllIncrement(String key) {
        byte[] rawKey = rawKey(key);
        Map<byte[], byte[]> entries = execute(connection -> connection.hGetAll(rawKey), true);
        Map<String, Long> result = Maps.newHashMap();
        for (byte[] k : entries.keySet()) {
            result.put((String) redisTemplate.getHashKeySerializer().deserialize(k), new Long(new String(entries.get(k))));
        }
        return result;
    }

    <T> T execute(RedisCallback<T> callback, boolean b) {
        return (T) redisTemplate.execute(callback, b);
    }

    byte[] rawKey(Object key) {

        Assert.notNull(key, "non null key required");

        if (keySerializer() == null && key instanceof byte[]) {
            return (byte[]) key;
        }

        return keySerializer().serialize(key);
    }

    <HK> byte[] rawHashKey(HK hashKey) {
        Assert.notNull(hashKey, "non null hash key required");
        if (hashKeySerializer() == null && hashKey instanceof byte[]) {
            return (byte[]) hashKey;
        }
        return hashKeySerializer().serialize(hashKey);
    }


    RedisSerializer keySerializer() {
        return redisTemplate.getKeySerializer();
    }

    RedisSerializer hashKeySerializer() {
        return redisTemplate.getHashKeySerializer();
    }

