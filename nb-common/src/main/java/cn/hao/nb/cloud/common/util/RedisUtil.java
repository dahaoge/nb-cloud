    /**
     * 分布锁
     *
     * @param key
     * @param expTime(秒)
     * @param val
     * @return
     */
    public boolean lock(String key, String val, long expTime) {
        String lock = LOCK_PREFIX + key;
        // 利用lambda表达式
        return (Boolean) redisTemplate.execute((RedisCallback) connection -> {

            long expireAt = System.currentTimeMillis() + (expTime * 1000) + 1;
            Boolean acquire = connection.setNX(lock.getBytes(), String.valueOf(expireAt + "," + val).getBytes());

            if (acquire) {
                return true;
            } else {
                byte[] value = connection.get(lock.getBytes());

                if (Objects.nonNull(value) && value.length > 0) {

                    long expireTime = Long.parseLong(new String(value).split(",")[0]);

                    if (expireTime < System.currentTimeMillis()) {
                        // 如果锁已经过期
                        byte[] oldValue = connection.getSet(lock.getBytes(), String.valueOf(expireAt + "," + val).getBytes());
                        // 防止死锁
                        return Long.parseLong(new String(oldValue).split(",")[0]) < System.currentTimeMillis();
                    }
                }
            }
            return false;
        });
    }

    public Boolean releaseLock(String key) {
        String lock = LOCK_PREFIX + key;
        return (Boolean) redisTemplate.execute((RedisCallback) connection -> {
            return connection.del(lock.getBytes()) > 0;
        });
    }

    public String getLockVal(String key) {
        String lock = LOCK_PREFIX + key;
        String str = (String) redisTemplate.execute((RedisCallback) connection -> {
            byte[] value = connection.get(lock.getBytes());
            if (Objects.nonNull(value) && value.length > 0)
                return new String(value);
            return "";
        });
        if (CheckUtil.strIsEmpty(str)) {
            return null;
        } else {
            long expireTime = Long.parseLong(str.split(",")[0]);

            if (expireTime < System.currentTimeMillis()) {
                // 如果锁已经过期
                this.releaseLock(lock);
                // 防止死锁
                return null;
            } else {
                return str.split(",")[1];
            }
        }
    }

